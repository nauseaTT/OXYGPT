from typing import List, Dict, Tuple
from dataclasses import dataclass, field

# ============================================================
# TOOL EVENT - Structured callback data for animator updates
# ============================================================


@dataclass
class ToolEvent:
    """Carries structured information from tool execution to the animator.

    Each tool call produces a ToolEvent that the animator uses to render
    context-aware, tool-specific animations instead of generic labels.

    Attributes:
        tool: Tool identifier — "search" | "image" | "market" | "html".
        label: Human-readable label shown in the animation header.
        query: The search query or image prompt the model provided.
        sources: List of dicts with "uri" and "title" keys (search results).
        progress: Normalized progress 0.0–1.0 (used by image generation).
        stage: Current execution stage identifier for the animator.
        detail: Extra text shown below the label (e.g. symbol, style).
    """

    tool: str
    label: str
    query: str = ""
    sources: List[Dict[str, str]] = field(default_factory=list)
    progress: float = 0.0
    stage: str = ""
    detail: str = ""


